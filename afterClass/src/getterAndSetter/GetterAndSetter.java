package getterAndSetter;

public class GetterAndSetter {

	private int age;
	private boolean num;
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public boolean isNum() {
		return num;
	}
	public void setNum(boolean num) {
		this.num = num;
	}
	
	

}
